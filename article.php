							<p class="big">En amont de la COP21 qui aura lieu à Paris en décembre 2015, le sommet mondial Climat et territoires, la société en mouvement s’est déroulé à Lyon les 2 et 3 juillet 2015.</p>
							<p>Les objectifs du sommet étaient les suivants&nbsp;:</p>
							<ul>
								<li>Présenter les engagements concrets pris par l’ensemble des acteurs territoriaux. Les annonces de réduction de gaz à effet de serre faites à Lyon doivent contribuer à crédibiliser le scénario de limitation du réchauffement de la planète à 2°C.</li>
								<li>Défendre des propositions précises pour que les engagements des acteurs territoriaux puissent trouver leur place dans un accord ambitieux à Paris.</li>
							</ul>

							<p>La session d’ouverture a été introduite par une allocution du président de la région Rhône-Alpes, félicitant l’ampleur de la rencontre et l’importance du soutien collectif pour la réussite de la Cop 21. Michael Bloomberg, ex-maire de New-York a évoqué le sommet des gouvernements locaux prévu à Paris en marge de la Cop21 et a insisté sur le fait que les villes ont les moyens de prendre trois fois plus d’initiatives dans la lutte contre le changement climatique, et de donner une certaine efficacité aux mesures prises. Il est également revenu sur l’importance du pacte mondial des maires, cadre appelant à fixer des objectifs explicites et à mettre en œuvre la participation des villes&nbsp;; chaque ville ayant également ses défis propres.</p>

							<p>La session «&nbsp;coopération décentralisée et partenariats entre territoires&nbsp;» a insisté sur l’importance de la mise en réseau des territoires et du partenariat entre les acteurs locaux pour «&nbsp;partager de la résilience&nbsp;». Il s’agit de trouver des solutions et de les mettre en œuvre afin de travailler à d’autres échelles et des multi-niveaux. Plusieurs «&nbsp;bonnes pratiques&nbsp;» mises en place ont été présentées à titre d’exemple, notamment&nbsp;:</p>
							<ul>
								<li>Le partenariat entre la région de Dakar (Sénégal) et le Conseil Régional d’Ile de France (France)&nbsp;: la région de Dakar a sollicité la région Ile de France pour l’élaboration de son plan climat territorial intégré (PCTI, le premier en Afrique). Ce PCTI concerne autant l’atténuation que l’adaptation au changement climatique.</li>
								<li>Le partenariat entre l’état du Minais Gérais (Brésil) et la région Nord Pas de Calais (France)&nbsp;: ce partenariat de coopération décentralisée est orienté autour de l’élaboration d’un PCTI dans l’Etat Brésilien, portant sur l’adaptation au changement climatique et la réduction des émissions. Ce projet a été porté par deux agences pour l’environnement, l’ADEME (France) et la FEAM (Minais Gérais).</li>
							</ul>

							<p>L’agence de la Francophonie s’est engagée à financer 100 villes pour 2020, quatre milliards d’euros étant déjà libérés pour des projets urbains d’adaptation et d’atténuation depuis 2010 en intégrant les villes du Sud.</p>

							<p>Durant l’après-midi du 1er juillet, trois sessions ont eu lieu concernant «&nbsp;climat&nbsp;: la société en mouvement&nbsp;».
								<br class="autobr"> La session 2, intitulée «&nbsp;Jeunesse et climat&nbsp;: la parole aux responsables de demain&nbsp;», a présenté les témoignages de jeunes représentants des différents continents. Ils ont pu exposer la mise en place de solutions concrètes locales et régionales. Par exemple, le canadien Charles-Hugo Maziade de la Coopérative Forêt d’Arden a présenté son travail basé sur une approche éducative pour faire face au changement climatique et contribuer à la bourse du carbone. Son travail se déroule essentiellement dans les écoles auprès de jeunes de 9 à 12 ans qui ont la faculté de pouvoir influencer leur famille.</p>

							<iframe src="https://www.youtube.com/embed/AHdnmJ2G1P8" allowfullscreen="" frameborder="0" height="315" width="420"></iframe>

							<p>Différentes activités ont également eu lieu au sein de la ville de Lyon afin de montrer les exemples d’applications possibles, comme la visite de l’éco-quartier de La Duchère, celle du quartier de Confluence («&nbsp;territoire zéro carbone&nbsp;»), etc.</p>

							<p>L’importance du soutien aux élus locaux comme première ligne d’action face au changement climatique a été largement soulignée, de même que le potentiel de stimulation économique du changement économique, au-delà de l’aspect problématique. Enfin, ce sommet a été l’occasion pour la Chine de dévoiler son engagement dans la COP21.</p>

